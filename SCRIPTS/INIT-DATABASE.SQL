/* 
===============================================
CREATE DATABASE AND SCHEMAS 
===============================================
SCRIPT PURPOSE:
                 THIS SCRIPTS CREATE A NEW DATA BASE NAMED  'DATAWAREHOUSE' AFTER CHECKING IF IT ALREADY EXISTS.
                 IF THE DATABASE ALREADY EXISTS ,IT IS DROPPED AND RECREATED,ADDITIONALLY THE SCRIPT USES THREE SCHEMAS 
                  WITHIN THE DATABASE : 'BRONZE','SILVER' AND 'GOLD'.
WARNING:
                 RUNNING THIS SCRIPT WILL DROPPING THE ENTIRE 'DATAWAREHOUSE' DATABASE IF IT EXISTS.
                ALL DATA IN THE DATAWAREHOUSE WILL BE PERMANENTLY DELETED.PROCEED WITH CAUTION 
                AND ENSURE YOU HAVE A PROPER BACK UPS BEFORE RUNNING THE SRIPT.
*/
USE MASTER;
GO 



---DROP AND CREATE DATAWAREHOUSE PROJECT -----
IF EXISTS(SELECT 1 FROM DATABASES NAME=DATAWAREHOUSE)
BEGIN 
     ALTER DATABASE DATAWAREHOUSE SET SINGLE_USER WITH ROLLBACK IMMEDIATELY;
     DROP DATABASE DATAWAREHOUSE 
END ;
GO 

---CREATE DATABASE DATAWAREHOUSE ---
CREATE DATABASE DATAWAREHOUSE ;
GO 

USE DATAWAREHOUSE;
GO

CREATE SCHEMA BRONZE;
GO 

CREATE SCHEMA SILVER;
GO

CREATE SCHEMA GOLD;
GO 














                 
